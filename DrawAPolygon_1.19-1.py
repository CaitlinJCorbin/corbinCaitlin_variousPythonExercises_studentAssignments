import turtle               #imports the "drawing" ability
turtle.penup()              #prepares to draw
turtle.goto(40,-69.28)      #goes to first coordinate
turtle.pendown()            #makes first point
turtle.goto(-40,-69.28)     #the following lines are coordinates that draw the polygon
turtle.goto(-80,-9.8)       
turtle.goto(-40,69)
turtle.goto(40,69)
turtle.goto(80,0)
turtle.goto(40,-69.28)
turtle.done                 #end





